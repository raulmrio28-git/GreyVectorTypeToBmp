GB_INT32	UnicodeSection_GetIndex(GB_UINT16 nCode)
{
  int i; 

  if ( nCode >= 0x4E00u && nCode <= 0x9FBFu )
    return 114;
  for ( i = 0; i < UNICODE_SECTION_NUM; ++i )
  {
    if ( g_Unicode_Section[i].nMinCode <= (GB_INT32)nCode && (GB_UINT16)word_2[2 * i] >= (GB_INT32)nCode )
      return i;
  }
  return UNICODE_SECTION_NUM;
}

GB_INT32	UnicodeSection_GetSectionNum(GB_INT32 index)
{
  return word_2[2 * index] - g_Unicode_Section[index].nMinCode + 1;
}

void		UnicodeSection_GetSectionInfo(GB_INT32 index, GB_UINT16 *pMinCode, GB_UINT16 *pMaxCode)
{
  if ( pMinCode )
    *pMinCode = g_Unicode_Section[index].nMinCode;
  if ( pMaxCode )
    *pMaxCode = word_2[2 * index];
}